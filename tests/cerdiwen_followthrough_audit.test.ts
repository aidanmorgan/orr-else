// ---------------------------------------------------------------------------
// Type definitions (mirrors the manifest schema)
// ---------------------------------------------------------------------------

interface AuditEntry {
  beadId: string;
  title: string;
  impactAreas: string[];
  cerdiwenSurface: string;
  coverage: 'edge' | 'no-impact';
  followThroughBeadId?: string;
  noImpactReason?: string;
}

// ---------------------------------------------------------------------------
// Load the manifest once
// ---------------------------------------------------------------------------

function loadManifest(): AuditEntry[] {
  expect(
    fs.existsSync(MANIFEST_PATH),
    `Manifest not found at ${MANIFEST_PATH} — run bead 6q0y.50 to generate it`
  ).toBe(true);
  const raw = fs.readFileSync(MANIFEST_PATH, 'utf8');
  return JSON.parse(raw) as AuditEntry[];
}

const MANIFEST = loadManifest();

// ---------------------------------------------------------------------------
// (a) Every entry is well-formed
// ---------------------------------------------------------------------------

describe('(a) every audit entry is well-formed', () => {
  it('manifest is non-empty', () => {
    expect(MANIFEST.length).toBeGreaterThan(0);
  });

  it.each(MANIFEST.map(e => [e.beadId, e] as [string, AuditEntry]))(
    'entry %s: non-empty beadId and title',
    (_, entry) => {
      expect(entry.beadId.trim().length, `beadId must be non-empty`).toBeGreaterThan(0);
      expect(entry.title.trim().length, `title must be non-empty for ${entry.beadId}`).toBeGreaterThan(0);
    }
  );

  it.each(MANIFEST.map(e => [e.beadId, e] as [string, AuditEntry]))(
    'entry %s: at least one impactArea',
    (_, entry) => {
      expect(
        Array.isArray(entry.impactAreas) && entry.impactAreas.length >= 1,
        `${entry.beadId} must have at least one impactArea`
      ).toBe(true);
    }
  );

  it.each(MANIFEST.map(e => [e.beadId, e] as [string, AuditEntry]))(
    'entry %s: EXACTLY ONE of followThroughBeadId (edge) or noImpactReason (no-impact) — never both, never neither',
    (_, entry) => {
      const hasEdge = entry.coverage === 'edge' && typeof entry.followThroughBeadId === 'string' && entry.followThroughBeadId.length > 0;
      const hasNoImpact = entry.coverage === 'no-impact' && typeof entry.noImpactReason === 'string' && entry.noImpactReason.length > 0;

      const bothPresent = typeof entry.followThroughBeadId !== 'undefined' && typeof entry.noImpactReason !== 'undefined';
      const neitherPresent = typeof entry.followThroughBeadId === 'undefined' && typeof entry.noImpactReason === 'undefined';

      expect(
        bothPresent,
        `${entry.beadId}: must not have BOTH followThroughBeadId and noImpactReason`
      ).toBe(false);

      expect(
        neitherPresent,
        `${entry.beadId}: must have EITHER followThroughBeadId (coverage=edge) or noImpactReason (coverage=no-impact)`
      ).toBe(false);

      expect(
        hasEdge || hasNoImpact,
        `${entry.beadId}: coverage="${entry.coverage}" must match populated field — ` +
        `edge requires non-empty followThroughBeadId; no-impact requires non-empty noImpactReason`
      ).toBe(true);
    }
  );
});

// ---------------------------------------------------------------------------
// (b) AC7 beads are present with resolved coverage
// ---------------------------------------------------------------------------

describe('(b) AC7 beads (l3sw, t6gw, 85bl, 0ipk) are present with resolved coverage', () => {
  const byId = new Map(MANIFEST.map(e => [e.beadId, e]));

  it.each(AC7_BEAD_IDS)(
    'AC7 bead %s is in the manifest',
    (beadId) => {
      expect(
        byId.has(beadId),
        `AC7 bead ${beadId} must be present in the audit manifest`
      ).toBe(true);
    }
  );

  it('l3sw has coverage=edge pointing to 6q0y.10 (orchestrator follow-through edge)', () => {
    const entry = byId.get('l3sw');
    expect(entry).toBeDefined();
    expect(entry!.coverage).toBe('edge');
    expect(entry!.followThroughBeadId).toBe('6q0y.10');
  });

  it('t6gw has coverage=no-impact (Cerdiwen configures no retryPolicy)', () => {
    const entry = byId.get('t6gw');
    expect(entry).toBeDefined();
    expect(entry!.coverage).toBe('no-impact');
    expect(entry!.noImpactReason).toBeTruthy();
  });

  it('85bl has coverage=edge pointing to 6q0y.34 (orchestrator follow-through edge)', () => {
    const entry = byId.get('85bl');
    expect(entry).toBeDefined();
    expect(entry!.coverage).toBe('edge');
    expect(entry!.followThroughBeadId).toBe('6q0y.34');
  });

  it('0ipk has coverage=edge pointing to 6q0y.34 (orchestrator follow-through edge; idempotency side no-impact)', () => {
    const entry = byId.get('0ipk');
    expect(entry).toBeDefined();
    expect(entry!.coverage).toBe('edge');
    expect(entry!.followThroughBeadId).toBe('6q0y.34');
  });
});

