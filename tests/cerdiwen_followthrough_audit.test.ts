describe('(c) all impactAreas are in the canonical AC2 vocabulary (drift guard)', () => {
  it.each(MANIFEST.map(e => [e.beadId, e] as [string, AuditEntry]))(
    'entry %s: all impactAreas are canonical',
    (_, entry) => {
      for (const area of entry.impactAreas) {
        expect(
          CANONICAL_IMPACT_AREAS.has(area),
          `${entry.beadId}: impactArea "${area}" is not in the canonical AC2 vocabulary.\n` +
          `Allowed: ${[...CANONICAL_IMPACT_AREAS].sort().join(', ')}`
        ).toBe(true);
      }
    }
  );
});

// ---------------------------------------------------------------------------
// (d) No duplicate beadId
// ---------------------------------------------------------------------------

describe('(d) no duplicate beadId in manifest', () => {
  it('all beadIds in the manifest are unique', () => {
    const seen = new Set<string>();
    const duplicates: string[] = [];
    for (const entry of MANIFEST) {
      if (seen.has(entry.beadId)) {
        duplicates.push(entry.beadId);
      }
      seen.add(entry.beadId);
    }
    expect(
      duplicates,
      `Duplicate beadIds found in manifest: ${duplicates.join(', ')}`
    ).toHaveLength(0);
  });
});

// ---------------------------------------------------------------------------
// (e) Completeness / superset guard (AC6)
// Every known open harness bead must appear in the manifest.
// Adding a new open harness bead without auditing it FAILS this test.
// ---------------------------------------------------------------------------

describe('(e) completeness guard: manifest covers every known open harness bead', () => {
  const manifestIds = new Set(MANIFEST.map(e => e.beadId));

  it('manifest covers all known open harness bead IDs', () => {
    const missing = KNOWN_OPEN_HARNESS_BEAD_IDS.filter(id => !manifestIds.has(id));
    expect(
      missing,
      `The following known open harness beads are absent from the audit manifest:\n` +
      missing.map(id => `  - ${id}`).join('\n') +
      `\nAdd audit entries to tests/fixtures/cerdiwen-followthrough/harness-bead-audit.json.`
    ).toHaveLength(0);
  });

  it(`KNOWN_OPEN_HARNESS_BEAD_IDS has ${KNOWN_OPEN_HARNESS_BEAD_IDS.length} entries`, () => {
    // This count is load-bearing: adding a new harness bead requires updating
    // KNOWN_OPEN_HARNESS_BEAD_IDS (increment this expected value) AND adding
    // an entry to the manifest JSON.
    expect(KNOWN_OPEN_HARNESS_BEAD_IDS.length).toBe(107);
  });
});
