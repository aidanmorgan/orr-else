#!/usr/bin/env node
/**
 * orr-else init
 *
 * Scaffolds a consuming project's .pi/ directory from the bundled templates.
 * Writes a checkout-free extension shim that imports from the installed package.
 *
 * Usage: npx orr-else init [--force]
 */

import * as fs from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';
import { program } from 'commander';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// The templates/ directory ships alongside dist/ inside the installed package.
// From dist/bin/init.js, go up two levels to reach the package root, then into templates/.
const PACKAGE_ROOT = path.resolve(__dirname, '..', '..');
const TEMPLATES_DIR = path.join(PACKAGE_ROOT, 'templates');

// The installed package name (used in the shim import path).
const PACKAGE_NAME = 'orr-else';

interface InitOptions {
  force: boolean;
}

function ensureDir(dirPath: string): void {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
    console.log(`  created  ${dirPath}`);
  }
}

function writeFile(filePath: string, content: string, force: boolean): void {
  if (fs.existsSync(filePath) && !force) {
    console.log(`  skipped  ${filePath} (already exists — use --force to overwrite)`);
    return;
  }
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`  wrote    ${filePath}`);
}

function copyTemplate(
  templateRelPath: string,
  destPath: string,
  force: boolean
): void {
  const src = path.join(TEMPLATES_DIR, templateRelPath);
  if (!fs.existsSync(src)) {
    console.warn(`  warning  template not found: ${src}`);
    return;
  }
  const content = fs.readFileSync(src, 'utf8');
  writeFile(destPath, content, force);
}

function resolveInstalledVersion(projectRoot: string): string {
  // Try to read the installed version from node_modules/orr-else/package.json.
  try {
    const pkgPath = path.join(projectRoot, 'node_modules', PACKAGE_NAME, 'package.json');
    const raw = fs.readFileSync(pkgPath, 'utf8');
    const parsed = JSON.parse(raw) as { version?: string };
    if (typeof parsed.version === 'string' && parsed.version) {
      return parsed.version;
    }
  } catch {
    // Ignore — fall through to default.
  }
  return '1.0.0';
}

async function runInit(projectRoot: string, opts: InitOptions): Promise<void> {
  const force = opts.force;

  console.log(`\norr-else init — scaffolding .pi/ in ${projectRoot}\n`);

  // 1. Write the extension shim — imports from the installed package, NOT a checkout.
  const shimPath = path.join(projectRoot, '.pi', 'extensions', 'orr-else.ts');
  const shimContent = [
    `// Orr Else extension shim.`,
    `// This file was generated by \`orr-else init\`.`,
    `// It imports from the installed package — no path back to the orr-else source checkout.`,
    `import orrElse from '${PACKAGE_NAME}/dist/extension.js';`,
    `export default orrElse;`,
    ``
  ].join('\n');
  writeFile(shimPath, shimContent, force);

  // 2. Write / update .pi/settings.json to use the npm: package specifier.
  const settingsPath = path.join(projectRoot, '.pi', 'settings.json');
  const version = resolveInstalledVersion(projectRoot);
  const settingsContent = JSON.stringify(
    {
      packages: [
        `npm:${PACKAGE_NAME}@^${version}`
      ]
    },
    null,
    2
  ) + '\n';
  writeFile(settingsPath, settingsContent, force);

  // 3. Write a starter harness.yaml from template (if none exists).
  const harnessYamlPath = path.join(projectRoot, 'harness.yaml');
  copyTemplate('harness.yaml', harnessYamlPath, force);

  // 4. Scaffold .pi/prompts/, .pi/skills/, .pi/rules/ from templates.
  const piDirs = [
    path.join(projectRoot, '.pi', 'prompts'),
    path.join(projectRoot, '.pi', 'skills'),
    path.join(projectRoot, '.pi', 'rules'),
    path.join(projectRoot, '.pi', 'extensions'),
  ];
  for (const dir of piDirs) {
    ensureDir(dir);
  }

  // Copy template files for prompts, skills, rules.
  const templateMappings: Array<[string, string]> = [
    ['prompts/implementer.md', path.join(projectRoot, '.pi', 'prompts', 'implementer.md')],
    ['prompts/planner.md', path.join(projectRoot, '.pi', 'prompts', 'planner.md')],
    ['prompts/reviewer.md', path.join(projectRoot, '.pi', 'prompts', 'reviewer.md')],
    ['rules/harness_rules.md', path.join(projectRoot, '.pi', 'rules', 'harness_rules.md')],
    ['skills/README.md', path.join(projectRoot, '.pi', 'skills', 'README.md')],
  ];

  for (const [tmplRel, destPath] of templateMappings) {
    copyTemplate(tmplRel, destPath, force);
  }

  console.log(`
Done! Next steps:

  1. Load the harness in Pi:
       pi -e .pi/extensions/orr-else.ts

  2. Start the orchestrator inside that Pi session:
       /orr-else

  3. If Pi's package loader needs it, ensure your .pi/settings.json is active:
       cat .pi/settings.json

NOTE: ORR_ELSE_FRAMEWORK_ROOT is NOT needed for normal project use.
It is only required when developing the orr-else harness itself.
`);
}

program
  .name('orr-else')
  .description('Orr Else harness CLI');

program
  .command('init', { isDefault: false })
  .description('Scaffold .pi/ in the current project directory')
  .option('--force', 'Overwrite existing files', false)
  .option('--cwd <dir>', 'Project root directory (default: process.cwd())', process.cwd())
  .action(async (opts: { force: boolean; cwd: string }) => {
    const projectRoot = path.resolve(opts.cwd);
    await runInit(projectRoot, { force: opts.force });
  });

// If called with no arguments or only "init" is the first arg, run init by default.
const args = process.argv.slice(2);
if (args.length === 0 || (args[0] !== 'init' && !args[0]?.startsWith('-'))) {
  // Show help when called with no subcommand.
  program.help();
} else {
  program.parse(process.argv);
}
